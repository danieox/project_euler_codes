 long long int largest_product_in_series(char array[], int number_length, int number_adjacent, int *start_index){
     long long  int largest_product = 0;
    for (int a = 0; a <= number_length-number_adjacent; a++ ){
        long long int product = 1;
        for(int i = a ; i < number_adjacent + a; i++){
           long long int digit = array[i] - '0';
              product *= digit ;
         }
         if (product > largest_product){
         largest_product = product;
         *start_index = a;
      }
    }
    return largest_product;
}

